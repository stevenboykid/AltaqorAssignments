package altaqor.assignments.assignment11;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		//Arithmetic operators Addition(+),Subtraction(-),Multiplication(*),Division(/),Modulus(%).
		int x =50;
		int y =10;
	
		int sum = x+y;
		int sub = x-y;
		int mul = x*y;
		int div = x/y;
		int rem = x%y;
		System.out.println("Arithematic Operators are as Follows:\n");
		System.out.println("1st. Addition(x+y) = " +sum);
		System.out.println("2nd. Subtraction(x-y) = " +sub);
		System.out.println("3rd. Multiplication(x*y) = " +mul);
		System.out.println("4th. Division (x/y) = " +div);
		System.out.println("5th. Modulo (x%y) = " +rem);
		
	}
}
